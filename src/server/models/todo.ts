import {Schema, model} from "mongoose";

const todoSchema: Schema = new Schema({
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true,
  },
  completed: {
    type: Boolean,
    default: false,
  },
  completedAt: {
    type: Number,
    default: null,
  },
});

export default model("Todo", todoSchema);
